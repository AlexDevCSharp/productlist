// Нормализация: ё→е, нижний регистр
function normalize(s: string): string {
  return s.toLowerCase().replace(/ё/g, 'е').trim();
}

// Убираем количества, веса, комментарии
function cleanProductName(name: string): string {
  return name
    .replace(/\(.*?\)/g, '')
    .replace(/~?\d+[.,]?\d*\s*(кг|г|гр|л|мл|шт|пач|уп|бут|банк|штук|литр|пакет|пачек|пачк|упаков)\b\.?/gi, '')
    .replace(/^\d+\s*/g, '')
    .replace(/\b(большой|большая|большое|маленький|маленькая|маленьких|средний|средняя|или|штуки?|штук)\b/gi, '')
    .replace(/\s+/g, ' ')
    .trim();
}

