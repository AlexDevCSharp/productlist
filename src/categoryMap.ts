// Убираем количества, веса, комментарии
function cleanProductName(name: string): string {
  return name
    // убираем скобки с содержимым: "батат (если есть)" → "батат"
    .replace(/\(.*?\)/g, '')
    // убираем количества: "2 кг", "~2 кг", "1 большой или 3 маленьких", "500г", "0.5 л"
    .replace(/~?\d+[.,]?\d*\s*(кг|г|гр|л|мл|шт|пач|уп|бут|банк|штук|литр|пакет|пачек|пачк|упаков)\b\.?/gi, '')
    // убираем числа с единицами в начале: "2 шт курица"
    .replace(/^\d+\s*/g, '')
    // убираем "большой", "маленький", "или" и подобное
    .replace(/\b(большой|большая|большое|маленький|маленькая|маленьких|средний|средняя|или|штуки?|штук)\b/gi, '')
    // убираем множественные пробелы
    .replace(/\s+/g, ' ')
    .trim();
}

// Нормализация для поиска: ё→е, убираем лишнее
function normalize(s: string): string {
  return s.toLowerCase().replace(/ё/g, 'е').trim();
}

export function findCategory(productName: string): string {
  const cleaned = cleanProductName(productName);
  const lower = normalize(cleaned);

  if (!lower || lower.length < 2) return 'Другое';

  // Сначала ищем по полному вхождению
  for (const [category, stems] of stemMap) {
    for (const stem of stems) {
      const normStem = normalize(stem).trim();
      if (lower === normStem || lower.includes(normStem) || normStem.includes(lower)) {
        return category;
      }
    }
  }

  // Затем ищем по отдельным словам
  const words = lower.split(/\s+/);
  for (const [category, stems] of stemMap) {
    for (const stem of stems) {
      const normStem = normalize(stem).trim();
      if (words.some(w => w.startsWith(normStem) || normStem.startsWith(w) && w.length >= 3)) {
        return category;
      }
    }
  }

  return 'Другое';
}
