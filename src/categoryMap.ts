function checkModifiers(lower: string): string | null {
  for (const [keyword, category] of modifierMap) {
    if (lower.includes(normalize(keyword))) {
      return category;
    }
  }
  return null;
}

function checkStemMap(lower: string): string | null {
  const words = lower.split(/\s+/);

  // Сначала ищем по полному вхождению подстроки
  for (const [category, stems] of stemMap) {
    for (const stem of stems) {
      const ns = normalize(stem).trim();
      if (lower.includes(ns)) return category;
    }
  }

  // Затем ищем по отдельным словам (startsWith)
  for (const [category, stems] of stemMap) {
    for (const stem of stems) {
      const ns = normalize(stem).trim();
      if (ns.length < 3) continue;
      if (words.some(w => w.startsWith(ns) || (ns.startsWith(w) && w.length >= 3))) {
        return category;
      }
    }
  }

  return null;
}

export function findCategory(productName: string): string {
  const cleaned = cleanProductName(productName);
  const lower = normalize(cleaned);

  if (!lower || lower.length < 2) return 'Другое';

  // Модификаторы имеют приоритет над основным словарём
  const modifierResult = checkModifiers(lower);
  if (modifierResult) return modifierResult;

  return checkStemMap(lower) ?? 'Другое';
}
