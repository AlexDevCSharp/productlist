export function findCategory(productName: string): string {
  const lower = productName.toLowerCase().trim();
  for (const [category, products] of Object.entries(categoryMap)) {
    if (products.some(p => lower === p || lower.includes(p) || p.includes(lower))) {
      return category;
    }
  }
  return 'Другое';
}
